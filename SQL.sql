CREATE OR REPLACE PROCEDURE add_supplier(
    p_supplier_name    IN VARCHAR2,
    p_contact_name     IN VARCHAR2,
    p_email    IN VARCHAR2,
    p_phone            IN VARCHAR2,
    p_address          IN VARCHAR2,
    p_city             IN VARCHAR2,
    p_state            IN VARCHAR2,
    p_zip_code         IN VARCHAR2,
    p_username         IN VARCHAR2 DEFAULT NULL,
    p_nic                IN VARCHAR2,
    p_user_password    IN VARCHAR2 DEFAULT NULL
) AS
    v_user_id NUMBER;
BEGIN
    -- Check if User info is provided, if so, create a new user
    IF p_username IS NOT NULL AND p_user_password IS NOT NULL THEN
        -- Insert into Users table
        INSERT INTO Users (UserID, UserName, Password, NIC, Type)
        VALUES (
            Users_seq.NEXTVAL, 
            p_username,         
            p_user_password, 
            p_nic,
            'Supplier'          
        )
        RETURNING UserID INTO v_user_id;
    ELSE
        -- Set v_user_id to NULL if no user is provided
        v_user_id := NULL;
    END IF;

    -- Insert into Supplier table
    INSERT INTO Supplier (
        SupplierID,
        SupplierName,
        ContactName,
        Email,
        Phone,
        Address,
        City,
        State,
        ZipCode,
        UserID
    ) VALUES (
        Supplier_seq.NEXTVAL,
        p_supplier_name,
        p_contact_name,
        p_email,
        p_phone,
        p_address,
        p_city,
        p_state,
        p_zip_code,
        v_user_id
    );

    -- Commit the transaction
    COMMIT;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END;
/



